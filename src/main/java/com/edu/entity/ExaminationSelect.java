package com.edu.entity;

public class ExaminationSelect {
	
	private String slectA;
	private String slectB;
	private String slectC;
	private String slectD;
	public String getSlectA() {
		return slectA;
	}
	public void setSlectA(String slectA) {
		this.slectA = slectA;
	}
	public String getSlectB() {
		return slectB;
	}
	public void setSlectB(String slectB) {
		this.slectB = slectB;
	}
	public String getSlectC() {
		return slectC;
	}
	public void setSlectC(String slectC) {
		this.slectC = slectC;
	}
	public String getSlectD() {
		return slectD;
	}
	@Override
	public String toString() {
		return "ExaminationSelect [slectA=" + slectA + ", slectB=" + slectB
				+ ", slectC=" + slectC + ", slectD=" + slectD + "]";
	}
	public void setSlectD(String slectD) {
		this.slectD = slectD;
	}
	
}	